S="Ребята ищут шишки"

def f(A):
    a=A.lower()
    b = ''
    c = ''
    for i in range(len(a)):
        if (a[i] == 'ж' or a[i] == 'ш') and a[i + 1] == 'ы':
            b='Ошибка в сочетании жи/ши'
        if (a[i] == 'ч' or a[i] == 'щ') and a[i + 1] == 'я':
            c='Ошибка в сочетании ча/ща'
        if b=='' and c=='':
            b='Ошибок в сочетании жи/ши и ча/ща нет'
    print(b,c)

f(S)

